const mongoose = require("mongoose");

const jobUserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  role: {
    type: String,
    enum: ["company", "admin", "user"],
    required: true,
  },
  passwordHash: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const JobUser = mongoose.model("JobUser", jobUserSchema, "job_user");

module.exports = JobUser;
